"""Regenerate a human-readable summary of the radical-addition ledger.

Mirrors scripts/summarize.py for M0: reads the append-only addition ledger and
writes summary.md — the leaderboard (most favorable first) and coverage. The
narrative stays in the top-level JOURNAL.md.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

RESULTS = Path(__file__).parent / "results"


def build_summary(ledger_path: Path) -> str:
    latest: dict[str, dict] = {}
    if ledger_path.exists():
        for line in ledger_path.open(encoding="utf-8"):
            if line.strip():
                r = json.loads(line)
                latest[r["spec_id"]] = r  # later records supersede
    usable = [r for r in latest.values() if r.get("ok") and r.get("reaction_energy_kcal") is not None]
    usable.sort(key=lambda r: r["reaction_energy_kcal"])  # most favorable (most negative) first
    failed = [r for r in latest.values() if not r.get("ok")]
    methods = sorted({r.get("method") for r in usable if r.get("method")})

    lines = [
        "# M4 — radical addition: ledger summary",
        "",
        "_Auto-generated by `summarize.py`. The append-only ledger",
        "(`results/ledger.jsonl`) is the source of truth._",
        "",
        f"- distinct candidates: **{len(latest)}**",
        f"- usable measurements: **{len(usable)}**  "
        f"(all favorable: {all(r['reaction_energy_kcal'] < 0 for r in usable)})",
        f"- unusable / failures: **{len(failed)}**",
        f"- arbiter methods: {', '.join(m for m in methods if m) or 'n/a'}",
        "",
        "Reaction: `Tool· + alkene → Tool–CH2–CH2·` (anti-Markovnikov; radical",
        "on the more-substituted carbon). ΔE = E(adduct·) − E(Tool·) − E(alkene).",
        "",
        "## Leaderboard (most favorable first)",
        "",
        "| candidate | tool | substrate | ΔE (kcal/mol) | favorable |",
        "|-----------|------|-----------|--------------:|:---------:|",
    ]
    for r in usable:
        lines.append(
            f"| {r['spec_id']} | {r['tool']} | {r['substrate']} "
            f"| {r['reaction_energy_kcal']:+.1f} "
            f"| {'yes' if r['favorable'] else 'no'} |"
        )
    if failed:
        lines += ["", "## Unusable / failed", ""]
        for r in failed:
            lines.append(f"- `{r['spec_id']}` — {r.get('error', 'unknown')}")
    lines.append("")
    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    argv = argv if argv is not None else sys.argv[1:]
    ledger_path = Path(argv[0]) if argv else RESULTS / "ledger.jsonl"
    out = ledger_path.parent / "summary.md"
    out.write_text(build_summary(ledger_path), encoding="utf-8")
    print(f"wrote {out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
